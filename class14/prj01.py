#########################匯入模組#########################
import mcu
import time

#########################函式與類別定義#########################

#########################宣告與設定#########################
mp3 = mcu.MP3()
#########################主程式#########################
# 自動從10進制轉成16進制
# print(f"10進位的{100}轉為16進位是{hex(100)}")
mp3.start(volume=int(hex(100), 16), song=int(hex(72), 16))  # 播放第一首歌
time.sleep(5)  # 播放5秒
mp3.stop()  # 停止播放
